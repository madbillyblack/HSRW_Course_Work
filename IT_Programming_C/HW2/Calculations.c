#include <stdio.h>


int main()
{
    int a, b, c, d, e, f;
    a=2;
    b=3;
    c=5;
    d=6;
    e=11;
    f=14;
    printf("My calculations:\n");
    printf("%d + %d = %d \n", b, c, b + c);
    printf("%d - %d = %d \n", d, b, d - b);
    printf("%d * %d = %d \n", e, a, e * a);
    printf("%d / %d = %d \n", f, a, f / a);
    return 0;
}
